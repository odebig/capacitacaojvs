//Realize diversas operações aritméticas utilizando os operadores vistos na capacitação.

// Operações aritméticas básicas

const a = 10;
const b = 3;

// Soma
const soma = a + b;
console.log('Soma:', soma); // 13

// Subtração
const subtracao = a - b;
console.log('Subtração:', subtracao); // 7

// Multiplicação
const multiplicacao = a * b;
console.log('Multiplicação:', multiplicacao); // 30

// Divisão
const divisao = a / b;
console.log('Divisão:', divisao); // 3.3333...

// Módulo (resto da divisão)
const modulo = a % b;
console.log('Módulo:', modulo); // 1

// Potência
const potencia = a ** b;  // 10 elevado a 3
console.log('Potência:', potencia); // 1000

// Incremento
let c = 5;
c++;
console.log('Incremento:', c); // 6

// Decremento
let d = 5;
d--;
console.log('Decremento:', d); // 4
